console.log("O segundo valor de é", segundaVariavel)

//Agora, queremos trocar os valores delas, de forma que `a` passe a ter o valor de `b` e `b` passe a ter o valor de `a`.
// Aqui faremos uma lógica para trocar os valore
novaPrimeiraVariavel = (segundaVariavel)
novaSegundaVariavel = (primeiraVariavel)
console.log("O novo primeiro valor de é", novaPrimeiraVariavel)
